#include <bits/stdc++.h>
using namespace std;

class Task {
 public:
    void solve() {
        read_input();
        print_output(get_result());
    }

 private:
    int n, m;
    vector<int> dist;

    void read_input() {
        ifstream fin("in");
        fin >> n >> m;
        for (int i = 0, d; i < n; i++) {
            fin >> d;
            dist.push_back(d);
        }
        fin.close();
    }

    int get_result() {
        int fuel = m;
        int pitStops = 0;

        for (int i = 0; i < dist.size(); i++) {
            if (fuel >= dist[i] - dist[i - 1]) {
                fuel -= (dist[i] - dist[i - 1]);
            } else {
                pitStops++;
                fuel = m - (dist[i] - dist[i - 1]);
            }
        }

        return pitStops;
    }

    void print_output(int result) {
        ofstream fout("out");
        fout << result;
        fout.close();
    }
};

// Please always keep this simple main function!
int main() {
    // Allocate a Task object on heap in order to be able to
    // declare huge static-allocated data structures inside the class.
    Task *task = new Task();
    task->solve();
    delete task;
    return 0;
}
